INT -- whole number
INTEGER -- whole number

DECIMAL(m,n) -- decimal number with m digits before the decimal and n digits after the decimal

VARCHAR(n) -- string of length n

BLOB -- binary large object (binary large file)

DATE -- date (year, month, day) 'YYYY-MM-DD'
TIME -- time (hour, minute, second) 'HH:MM:SS'
TIMESTAMP -- date and time 'YYYY-MM-DD HH:MM:SS'